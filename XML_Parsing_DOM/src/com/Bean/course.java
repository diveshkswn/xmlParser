package com.Bean;

public class course {
	public String voyage;
	public String sharp;
	public String off;
	public String likely;
	public String crew;
	public String voice;
	
	
	@Override
	public String toString() {
		return "course [voyage=" + voyage + ", sharp=" + sharp + ", off=" + off + ", likely=" + likely + ", crew="
				+ crew + ", voice=" + voice + "]";
	}
	
	
	public String getVoyage() {
		return voyage;
	}
	public void setVoyage(String voyage) {
		this.voyage = voyage;
	}
	public String getSharp() {
		return sharp;
	}
	public void setSharp(String sharp) {
		this.sharp = sharp;
	}
	public String getOff() {
		return off;
	}
	public void setOff(String off) {
		this.off = off;
	}
	public String getLikely() {
		return likely;
	}
	public void setLikely(String likely) {
		this.likely = likely;
	}
	public String getCrew() {
		return crew;
	}
	public void setCrew(String crew) {
		this.crew = crew;
	}
	public String getVoice() {
		return voice;
	}
	public void setVoice(String voice) {
		this.voice = voice;
	}
	
	
	
}
